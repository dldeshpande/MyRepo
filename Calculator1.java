 class Calculator1
{

public int getAdd(int x,int y)
{
int result = x+y;
return result;
}
public int getSub(int x,int y)
{
int result = x-y;
return result;
}
public int getMult(int x,int y)
{
int result = x*y;
return result;
}
public int getDiv(int x,int y)
{
int result = x/y;
return result;
}

}